#!/usr/bin/env node
/**
 * Examanet Env Vars Documentation
 *
 * Reads all env vars (redacted), produces a markdown table showing
 * which vars exist, their lengths, and where they're documented.
 *
 * Usage: node scripts/backup/env-vars-doc.mjs
 *
 * Output: docs/operations/env-vars.md
 */

import fs from 'fs';
import path from 'path';

const ENV_FILES = ['.env', '.env.local', '.env.example', '.vercel/.env.development.local'];

// Vars we should NEVER log content of (just count chars)
const SECRET_VARS = [
  'DATABASE_URL', 'NEXTAUTH_SECRET', 'BLOB_READ_WRITE_TOKEN', 'RESEND_API_KEY',
  'OPENAI_API_KEY', 'JOTFORM_API_KEY', 'SEED_TOKEN', 'I_LOVE_API_KEY',
  'I_LOVE_API_PUBLIC_KEY', 'VERCEL_OIDC_TOKEN', 'GITHUB_TOKEN', 'GITLAB_TOKEN',
];

function loadEnvFile(file) {
  if (!fs.existsSync(file)) return [];
  const content = fs.readFileSync(file, 'utf8');
  return content
    .split('\n')
    .map((l) => l.trim())
    .filter((l) => l && !l.startsWith('#') && l.includes('='))
    .map((l) => {
      const [k, ...rest] = l.split('=');
      const v = rest.join('=').replace(/^['"]|['"]$/g, '');
      return { key: k.trim(), value: v };
    });
}

function isSecret(key) {
  return SECRET_VARS.includes(key) || /TOKEN|SECRET|KEY|PASSWORD/i.test(key);
}

const allVars = new Map();
for (const file of ENV_FILES) {
  const vars = loadEnvFile(file);
  for (const { key, value } of vars) {
    if (!allVars.has(key)) allVars.set(key, { sources: new Set(), value: null });
    const entry = allVars.get(key);
    entry.sources.add(file);
    if (value && !entry.value) entry.value = value;
  }
}

const lines = [
  '# Examanet Environment Variables',
  '',
  `> Auto-generated by \`scripts/backup/env-vars-doc.mjs\` on ${new Date().toISOString()}`,
  '',
  'This document lists every environment variable the project uses, where it is defined, and whether it is sensitive.',
  '',
  '**NEVER commit secret values to Git.** This file shows only keys, sources, and redacted placeholders.',
  '',
  '## How to restore',
  '',
  '1. Create `.env.local` at the project root',
  '2. Copy each variable below from the last known good source (1Password, Vercel dashboard, or this backup)',
  '3. Run `npx prisma generate` after editing',
  '4. Verify with `node -e "require(\'dotenv\').config(); console.log(process.env.DATABASE_URL?.length)"`',
  '',
  '## Production secrets in Vercel',
  '',
  'Production values live in **Vercel Dashboard → Project → Settings → Environment Variables**.',
  'Vercel OIDC token does NOT grant API access to read them back — you must copy them manually.',
  '',
  '## Variable inventory',
  '',
  '| Variable | Type | Defined in | Length |',
  '|----------|------|------------|--------|',
];

const sortedKeys = Array.from(allVars.keys()).sort();
for (const key of sortedKeys) {
  const { sources, value } = allVars.get(key);
  const type = isSecret(key) ? '🔒 secret' : '📋 public';
  const sourcesList = Array.from(sources).sort().join(', ');
  const len = value ? value.length : 0;
  const lenDisplay = isSecret(key) ? `🔒 ${len} chars` : `${len} chars`;
  lines.push(`| \`${key}\` | ${type} | ${sourcesList} | ${lenDisplay} |`);
}

lines.push('');
lines.push('## Required for production');
lines.push('');
lines.push('These must be set in Vercel dashboard for the prod environment:');
lines.push('');
for (const key of sortedKeys) {
  if (isSecret(key)) {
    lines.push(`- \`${key}\` (${allVars.get(key).value ? '✅ set' : '❌ MISSING'})`);
  }
}
lines.push('');
lines.push('## Verification');
lines.push('');
lines.push('After restoring, run:');
lines.push('');
lines.push('```bash');
lines.push('# Check all required vars are set');
lines.push('node scripts/backup/env-check.mjs');
lines.push('```');
lines.push('');

const out = lines.join('\n');
const outPath = 'docs/operations/env-vars.md';
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, out);
console.log(`[env-vars] Wrote ${outPath}`);
console.log(`[env-vars] ${sortedKeys.length} variables found across ${ENV_FILES.length} files`);
console.log(`[env-vars] ${sortedKeys.filter(isSecret).length} secrets, ${sortedKeys.filter((k) => !isSecret(k)).length} public`);
